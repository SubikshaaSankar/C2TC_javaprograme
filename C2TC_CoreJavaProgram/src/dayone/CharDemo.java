package dayone;

public class CharDemo {
public static void main(String[] args) {
		
		char ch = 'a';
		System.out.println(ch);

		
		char ch1 = 85;
		System.out.println(ch1);

		
		char var1 = '\u00A7';
		System.out.println(var1);

		
		char var2 = '\u20AC';
		System.out.println(var2);

		
		int a = 'S';
		System.out.println(a);
	}


}
